#Program for Performing Arithmetic Operations

a=int(input("\t\tEnter value of A : "))
b=int(input("\t\tEnter Value of B : "))

print("="*50)
print("\t\t Sum ({} and {})={}".format(a,b,a+b))
print(" \t\t Substracion ({} and {})={}".format(a,b,a-b))
print(" \t \t Multiplication ({} and {})={}".format(a,b,a*b))
print(" \t \t Division ({} and {} )={}".format(a,b,a/b))
print(" \t \t Modulo Division ({} and {})={}".format(a,b,a//b))
print("\t\t Power ({} and {})={}".format(a,b,a**b))

print("="*50)
