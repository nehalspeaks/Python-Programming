# This program will help us to Demonstrate the functionality of Logical " And Operator "

print(10>2 and 10>30)   #--> True and False --> False ---> #Full Length Evaluation
print("-"*20)

print(10>20 and 20>10)  #--> False and True --> False ---> #Short Length Evaluation
print("-"*20)

print(10>20 and 20>30 and 10>4) #--> False and False and True --> False ---> #Short Length Evaluation
print("-"*20)

print(10>2 and 30>20 and 10>2)  #---> True and True and True ---> True ---> Full Length Evaluation
print("-"*20)

